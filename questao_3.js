let INDICE = 12, SOMA = 0, K = 1; // Inicializa as variáveis 'INDICE', 'SOMA' e 'K' com os valores 12, 0 e 1, respectivamente
while (K < INDICE) { // Continua o loop enquanto 'K' for menor que 'INDICE'
    K = K + 1; // Incrementa 'K' em 1
    SOMA = SOMA + K; // Adiciona o valor de 'K' à variável 'SOMA'
}
console.log(SOMA); // Imprime o valor final de 'SOMA'
